function createRunCluster({
  idPrefix,
  runIndexStart,
  segmentGroupIndex,
  startTile,
  lastTiles,
  startMinute,
  baseSecond,
  pitch,
  songPosition,
  note,
}: {
  idPrefix: string;
  runIndexStart: number;
  segmentGroupIndex: number;
  startTile: number;
  lastTiles: number[];
  startMinute: number;
  baseSecond: number;
  pitch: number;
  songPosition: number;
  note: string;
}) {
  return lastTiles.map((lastTile, index) => {
    const startedAt = makeJun29Time(startMinute + Math.floor(index * 0.85), baseSecond + (index * 17) % 45);
    const endedAt = makeJun29Time(startMinute + Math.floor(index * 0.85), baseSecond + 18 + (index * 17) % 45);
    const hitCount = Math.max(1, lastTile - startTile);

    return {
      Id: `${idPrefix}-${String(index + 1).padStart(2, "0")}`,
      RunIndex: runIndexStart + index,
      SegmentGroupIndex: segmentGroupIndex,
      StartedAtUtc: startedAt,
      EndedAtUtc: endedAt,
      StartTile: startTile,
      LastTile: lastTile,
      Result: lastTile === 4356 ? "completed" : "failed",
      NoFailMode: false,
      GameplayStartSongPosition: songPosition,
      LevelPitchPercent: pitch + (index > lastTiles.length / 2 ? 2 : 0),
      EffectivePitch: (pitch + (index > lastTiles.length / 2 ? 2 : 0)) / 100,
      InputCount: Math.max(18, Math.round(hitCount * 1.7)),
      HitContextCount: hitCount,
      Meta: {
        source: "mock",
        note,
        pitch: pitch + (index > lastTiles.length / 2 ? 2 : 0),
        capturedAtUtc: endedAt,
      },
    };
  });
}

function makeJun29Time(minuteOffset: number, second: number) {
  const totalSeconds = minuteOffset * 60 + second;
  const minute = 31 + Math.floor(totalSeconds / 60);
  const normalizedSecond = totalSeconds % 60;
  return `2026-06-29T08:${String(minute).padStart(2, "0")}:${String(normalizedSecond).padStart(2, "0")}.0000000Z`;
}

